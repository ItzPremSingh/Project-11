# Write a program in python to calculate speed of a vehicle for entered distance and time.

distance = int(input("Enter the distance (in metre): "))
time = int(input("Enter the time (in second): "))

speed = distance / time

print("Speed of vehicle is", speed, "metre/second")


# Output

# Enter the distance (in metre): 100
# Enter the time (in second): 20
# Speed of vehicle is 5.0 metre/second
